class DBSCAN {
    constructor(data, eps, minPts) {
        this.data = data; // 数据集
        this.eps = eps; // 密度阈值
        this.minPts = minPts; // 最小样本数
        this.clusters = []; // 保存聚类结果的数组
        this.visited = new Set(); // 用于标记数据点是否已被访问
        this.noise = new Set(); // 用于标记噪声点
    }

    // 计算两个点之间的欧氏距离
    euclideanDistance(point1, point2) {
        const [x1, y1] = point1;
        const [x2, y2] = point2;
        return Math.sqrt(Math.pow(x2 - x1, 2) + Math.pow(y2 - y1, 2));
    }

    // 根据指定点找到其邻居点
    findNeighbors(pointIndex) {
        const neighbors = [];
        for (let i = 0; i < this.data.length; i++) {
            if (i !== pointIndex) {
                const distance = this.euclideanDistance(this.data[pointIndex], this.data[i]);
                if (distance <= this.eps) {
                    neighbors.push(i);
                }
            }
        }
        return neighbors;
    }

    // 执行DBSCAN算法
    run() {
        for (let i = 0; i < this.data.length; i++) {
            if (!this.visited.has(i)) {
                this.visited.add(i);
                const neighbors = this.findNeighbors(i);
                if (neighbors.length >= this.minPts) {
                    // 若当前点是核心点，则生成新簇
                    const clusterId = this.clusters.length;
                    this.expandCluster(i, neighbors, clusterId);
                } else {
                    // 若当前点是噪声点，则将其加入噪声点集合
                    this.noise.add(i);
                }
            }
        }
        console.log("Clusters:", this.clusters);
        console.log("Noise:", this.noise);
    }

    // 扩展簇
    expandCluster(pointIndex, neighbors, clusterId) {
        const cluster = [pointIndex];
        this.clusters.push(cluster);
        for (const neighborIndex of neighbors) {
            if (!this.visited.has(neighborIndex)) {
                this.visited.add(neighborIndex);
                const neighborNeighbors = this.findNeighbors(neighborIndex);
                if (neighborNeighbors.length >= this.minPts) {
                    // 若邻居点是核心点，则将其加入当前簇
                    cluster.push(neighborIndex);
                    this.expandCluster(neighborIndex, neighborNeighbors, clusterId);
                }
            }
            // 若邻居点已在其他簇中，则将当前点加入噪声点集合
            if (!this.isInAnyCluster(neighborIndex)) {
                this.noise.add(neighborIndex);
            }
        }
    }

    // 判断指定点是否已在任何簇中
    isInAnyCluster(pointIndex) {
        for (const cluster of this.clusters) {
            if (cluster.includes(pointIndex)) {
                return true;
            }
        }
        return false;
    }
}

